    private void botonResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonResetActionPerformed
        // TODO add your handling code here:
        if(botonReset.getText().equals("RESET"))
        {
            password="";
            campoPassword.setText("");
        }
        else
        {
            final Empleado empleado=logicaNegocio.validarPassword(password);
           // System.out.println(empleado.getNombre());
             if(!empleado.getNombre().equals("N/A")){       
                    if(empleado.getCargo().equals("Mesero"))        
                    {
                        this.setVisible(false);
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                new VistaMesero(empleado).setVisible(true);
                            }
                        });

                    }
                    if(empleado.getCargo().equals("Cocina"))        
                    {
                        this.setVisible(false);
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                new VistaCocina(empleado).setVisible(true);
                            }
                        });

                    }
                        if(empleado.getCargo().equals("Cajero"))        
                    {
                        this.setVisible(false);
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                new VistaCajero(empleado).setVisible(true);
                            }
                        });

                    }
                     if(empleado.getCargo().equals("Administrador"))        
                    {
                        this.setVisible(false);
                        java.awt.EventQueue.invokeLater(new Runnable() {
                            public void run() {
                                new VistaAdministrador(empleado).setVisible(true);
                            }
                        });

                    }
                }
                 else{
                    campoPassword.setText("ERROR");                
                    botonReset.setText("RESET");
                    password="";
                 }
            
        }
        
    }//GEN-LAST:event_botonResetActionPerformed

