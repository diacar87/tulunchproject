    private void generarMenuDelDia() {
        javax.swing.table.DefaultTableModel modeloTabla = new javax.swing.table.DefaultTableModel();
         modeloTabla.addColumn("id");
         modeloTabla.addColumn("Nombre");             
         modeloTabla.addColumn("Tipo");  
         modeloTabla.addColumn("Precio");
         
         List<Producto> menu=MenuDelDia.getMenuDelDia();
         modeloTabla.setRowCount(menu.size());
                
                    for(int fila = 0 ; fila < menu.size() ; fila++){
                        modeloTabla.setValueAt(menu.get(fila).getId(), fila ,0);
                        modeloTabla.setValueAt(menu.get(fila).getNombre(), fila ,1);
                        modeloTabla.setValueAt(menu.get(fila).getTipo(), fila ,2);
                        modeloTabla.setValueAt(menu.get(fila).getPrecio(), fila ,3);                   
                    }                
                    tablaMenu.setModel(modeloTabla);         
         
    }
